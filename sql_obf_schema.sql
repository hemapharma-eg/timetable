-- OBF KPI Management Schema

-- 1. KPI Categories
CREATE TABLE IF NOT EXISTS public.obf_categories (
    id bigint generated by default as identity primary key,
    value text NOT NULL,
    label text NOT NULL,
    sort_order integer DEFAULT 0,
    created_at timestamp with time zone DEFAULT now()
);

ALTER TABLE public.obf_categories ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow full access on obf_categories" ON public.obf_categories FOR ALL USING (true) WITH CHECK (true);

